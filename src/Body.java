public class Body {
    public double mass;
    public double vx,vy;
    public double x,y;


    //construct
    public Body(double mass, double vx, double vy, double x, double y) {
        this.mass = mass;
        this.vx = vx;
        this.vy = vy;
        this.x = x;
        this.y = y;
    }


}
